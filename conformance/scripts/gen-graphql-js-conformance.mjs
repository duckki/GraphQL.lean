#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const CONFORMANCE_DIR = path.resolve(SCRIPT_DIR, '..');
const REPO_ROOT = path.resolve(CONFORMANCE_DIR, '..');

const DEFAULT_CASE_DIR = path.join(CONFORMANCE_DIR, 'graphql-js/cases');
const DEFAULT_OUTPUT = path.join(
  REPO_ROOT,
  'Tests/Conformance/Execution/GraphQLJS.lean',
);
const DEFAULT_NAMED_FRAGMENT_OUTPUT =
  path.join(REPO_ROOT, 'Tests/Conformance/NamedFragment/GraphQLJS.lean');

const args = parseArgs(process.argv.slice(2));
const caseDir = args.cases === undefined ? DEFAULT_CASE_DIR : path.resolve(args.cases);
const target = args.target ?? 'execution';
const outputPath =
  args.out === undefined
    ? target === 'named-fragment'
      ? DEFAULT_NAMED_FRAGMENT_OUTPUT
      : DEFAULT_OUTPUT
    : path.resolve(args.out);

const suites = readSuites(caseDir);
const cases = suites.flatMap((suite) =>
  suite.cases.map((testCase) => ({ ...testCase, suite: suite.suite })),
);

assertUniqueIds(cases);

if (!['execution', 'named-fragment'].includes(target)) {
  throw new Error(`unsupported target: ${target}`);
}

const generated = renderModule(cases, target);
fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, generated);
console.log(`wrote ${outputPath} (${cases.length} cases)`);

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === '--cases') {
      parsed.cases = argv[++index];
    } else if (arg === '--out') {
      parsed.out = argv[++index];
    } else if (arg === '--target') {
      parsed.target = argv[++index];
    } else if (arg === '--help' || arg === '-h') {
      console.log(
        [
          'Usage: node conformance/scripts/gen-graphql-js-conformance.mjs [--cases DIR] [--out FILE] [--target execution|named-fragment]',
          '',
          `Defaults: --cases ${displayPath(DEFAULT_CASE_DIR)} --out ${displayPath(DEFAULT_OUTPUT)} --target execution`,
        ].join('\n'),
      );
      process.exit(0);
    } else {
      throw new Error(`unknown argument: ${arg}`);
    }
  }
  return parsed;
}

function displayPath(filePath) {
  const relative = path.relative(process.cwd(), filePath);
  return relative === '' ? '.' : relative;
}

function readSuites(dir) {
  const entries = fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith('.json'))
    .map((entry) => path.join(dir, entry.name))
    .sort();

  return entries.map((file) => {
    const suite = JSON.parse(fs.readFileSync(file, 'utf8'));
    if (!Array.isArray(suite.cases)) {
      throw new Error(`${file} must contain a cases array`);
    }
    return suite;
  });
}

function assertUniqueIds(testCases) {
  const seen = new Set();
  for (const testCase of testCases) {
    assertIdentifier(testCase.id, 'case id');
    if (seen.has(testCase.id)) {
      throw new Error(`duplicate case id: ${testCase.id}`);
    }
    seen.add(testCase.id);
  }
}

function renderModule(testCases, target) {
  const namespaceName = target === 'named-fragment' ? 'NamedFragment' : 'Execution';
  const commonImport =
    target === 'named-fragment'
      ? 'import Tests.Conformance.Execution.Common'
      : 'import Tests.Conformance.Execution.Common';
  const chunks = [
    '/- Generated by conformance/scripts/gen-graphql-js-conformance.mjs. -/',
    commonImport,
    ...(target === 'named-fragment'
      ? ['import GraphQL.NamedFragment.Execution']
      : []),
    '',
    'namespace GraphQL',
    'namespace Tests',
    'namespace Conformance',
    `namespace ${namespaceName}`,
    'namespace GraphQLJS',
    '',
  ];

  for (const testCase of testCases) {
    chunks.push(renderCase(testCase, target));
  }

  chunks.push(
    'end GraphQLJS',
    `end ${namespaceName}`,
    'end Conformance',
    'end Tests',
    'end GraphQL',
    '',
  );

  return chunks.join('\n');
}

function renderCase(testCase, target) {
  const name = testCase.id;
  const fuel = testCase.fuel ?? 100;
  const sourceNote = testCase.sourceTest
    ? `-- Source: ${testCase.sourceTest}`
    : `-- Suite: ${testCase.suite}`;
  const operationType =
    target === 'named-fragment'
      ? 'GraphQL.NamedFragment.Operation'
      : 'GraphQL.Operation';
  const executeFunction =
    target === 'named-fragment'
      ? 'GraphQL.NamedFragment.Execution.executeQueryWithFuel'
      : 'GraphQL.Execution.executeQueryWithFuel';
  const responseEq =
    target === 'named-fragment'
      ? 'GraphQL.Tests.Conformance.Execution.responseEqBool'
      : 'responseEqBool';

  return [
    sourceNote,
    `def ${name}Schema : GraphQL.Schema :=`,
    indent(renderSchema(testCase.schema), 2),
    '',
    `def ${name}Operation : ${operationType} :=`,
    indent(renderOperation(testCase.operation, target), 2),
    '',
    `def ${name}Variables : GraphQL.Execution.VariableValues :=`,
    indent(renderVariables(testCase.variables ?? []), 2),
    '',
    `def ${name}Source : GraphQL.Execution.ResolverValue String :=`,
    indent(renderResolverValue(testCase.source), 2),
    '',
    `def ${name}Resolvers : GraphQL.Execution.Resolvers String :=`,
    indent(renderResolvers(testCase.resolvers ?? []), 2),
    '',
    `def ${name}ExpectedData : GraphQL.Execution.ResponseValue :=`,
    indent(renderResponseValue(testCase.expected.data), 2),
    '',
    `theorem ${name}_matches_graphql_js_projection :`,
    `    let response :=`,
    `      ${executeFunction} ${name}Schema`,
    `        ${name}Resolvers ${name}Variables ${name}Operation ${fuel}`,
    `        ${name}Source`,
    `    response.errors = ${testCase.expected.errorCount}`,
    `      ∧ ${responseEq} response.data ${name}ExpectedData = true := by`,
    '  native_decide',
    '',
  ].join('\n');
}

function renderSchema(schema) {
  return [
    '{ queryType := ' + leanString(schema.queryType) + ',',
    '  types :=',
    indent(leanList(schema.types ?? [], renderTypeDefinition, 4), 4),
    '}',
  ].join('\n');
}

function renderTypeDefinition(typeDef) {
  switch (typeDef.kind) {
    case 'object':
      return [
        '.object',
        '  { name := ' + leanString(typeDef.name) + ',',
        '    fields :=',
        indent(leanList(typeDef.fields ?? [], renderFieldDefinition, 6) + ',', 6),
        '    interfaces := ' + renderNameList(typeDef.interfaces ?? []),
        '  }',
      ].join('\n');
    case 'interface':
      return [
        '.interface',
        '  { name := ' + leanString(typeDef.name) + ',',
        '    fields :=',
        indent(leanList(typeDef.fields ?? [], renderFieldDefinition, 6) + ',', 6),
        '    interfaces := ' + renderNameList(typeDef.interfaces ?? []),
        '  }',
      ].join('\n');
    case 'union':
      return [
        '.union',
        '  { name := ' + leanString(typeDef.name) + ',',
        '    members := ' + renderNameList(typeDef.members ?? []),
        '  }',
      ].join('\n');
    case 'enum':
      return [
        '.enum',
        '  { name := ' + leanString(typeDef.name) + ',',
        '    values := ' + renderNameList(typeDef.values ?? []),
        '  }',
      ].join('\n');
    case 'customScalar':
      return '.customScalar { name := ' + leanString(typeDef.name) + ' }';
    case 'inputObject':
      return [
        '.inputObject',
        '  { name := ' + leanString(typeDef.name) + ',',
        '    inputFields :=',
        indent(
          leanList(typeDef.inputFields ?? [], renderInputValueDefinition, 6) + ',',
          6,
        ),
        '  }',
      ].join('\n');
    default:
      throw new Error(`unsupported type definition kind: ${typeDef.kind}`);
  }
}

function renderFieldDefinition(field) {
  return (
    `{ name := ${leanString(field.name)}, outputType := ${renderTypeRef(field.type)}, ` +
    `arguments := ${leanList(field.arguments ?? [], renderInputValueDefinition, 4)} }`
  );
}

function renderInputValueDefinition(definition) {
  const defaultValue =
    definition.defaultValue === undefined
      ? 'none'
      : `some (${renderConstInputValue(definition.defaultValue)})`;
  return (
    `{ name := ${leanString(definition.name)}, ` +
    `inputType := ${renderTypeRef(definition.type)}, defaultValue := ${defaultValue} }`
  );
}

function renderTypeRef(typeRef) {
  switch (typeRef.kind) {
    case 'named':
      return `.named ${leanString(typeRef.name)}`;
    case 'list':
      return `.list (${renderTypeRef(typeRef.of)})`;
    case 'nonNull':
      return `.nonNull (${renderTypeRef(typeRef.of)})`;
    default:
      throw new Error(`unsupported type ref kind: ${typeRef.kind}`);
  }
}

function renderOperation(operation, target) {
  const fields = [
    '{ name := ' + renderOptionString(operation.name) + ',',
    '  rootType := ' + leanString(operation.rootType) + ',',
    '  variableDefinitions :=',
    indent(
      leanList(operation.variableDefinitions ?? [], renderVariableDefinition, 4) + ',',
      4,
    ),
  ];
  if (target === 'named-fragment') {
    fields.push(
      '  fragmentDefinitions :=',
      indent(
        leanList(operation.fragmentDefinitions ?? [], renderFragmentDefinition, 4) + ',',
        4,
      ),
    );
  }
  fields.push(
    '  selectionSet :=',
    indent(
      leanList(
        operation.selectionSet ?? [],
        (selection) => renderSelection(selection, target),
        4,
      ),
      4,
    ),
    '}',
  );
  return fields.join('\n');
}

function renderFragmentDefinition(fragment) {
  return [
    '{',
    `  name := ${leanString(fragment.name)}`,
    `  typeCondition := ${leanString(fragment.typeCondition)}`,
    '  selectionSet :=',
    indent(
      leanList(
        fragment.selectionSet ?? [],
        (selection) => renderSelection(selection, 'named-fragment'),
        4,
      ),
      4,
    ),
    '}',
  ].join('\n');
}

function renderVariableDefinition(definition) {
  const defaultValue =
    definition.defaultValue === undefined
      ? 'none'
      : `some (${renderConstInputValue(definition.defaultValue)})`;
  return (
    `{ name := ${leanString(definition.name)}, ` +
    `typeRef := ${renderTypeRef(definition.type)}, defaultValue := ${defaultValue} }`
  );
}

function renderSelection(selection, target = 'execution') {
  switch (selection.kind) {
    case 'field':
      return [
        `.field ${leanString(selection.responseName)} ${leanString(selection.fieldName)}`,
        indent(leanList(selection.arguments ?? [], renderArgument, 2), 2),
        indent(leanList(selection.directives ?? [], renderDirective, 2), 2),
        indent(
          leanList(
            selection.selectionSet ?? [],
            (child) => renderSelection(child, target),
            2,
          ),
          2,
        ),
      ].join('\n');
    case 'inlineFragment':
      return [
        '.inlineFragment ' + renderOptionString(selection.typeCondition),
        indent(leanList(selection.directives ?? [], renderDirective, 2), 2),
        indent(
          leanList(
            selection.selectionSet ?? [],
            (child) => renderSelection(child, target),
            2,
          ),
          2,
        ),
      ].join('\n');
    case 'fragmentSpread':
      if (target !== 'named-fragment') {
        throw new Error('fragmentSpread requires --target named-fragment');
      }
      return [
        `.fragmentSpread ${leanString(selection.fragmentName)}`,
        indent(leanList(selection.directives ?? [], renderDirective, 2), 2),
      ].join('\n');
    default:
      throw new Error(`unsupported selection kind: ${selection.kind}`);
  }
}

function renderArgument(argument) {
  return `{ name := ${leanString(argument.name)}, value := ${renderInputValue(argument.value)} }`;
}

function renderDirective(directive) {
  switch (directive.kind) {
    case 'skip':
      return `.skip (${renderInputValue(directive.if)})`;
    case 'include':
      return `.include (${renderInputValue(directive.if)})`;
    default:
      throw new Error(`unsupported directive kind: ${directive.kind}`);
  }
}

function renderVariables(variables) {
  return leanList(
    variables,
    (variable) => `(${leanString(variable.name)}, ${renderInputValue(variable.value)})`,
    2,
  );
}

function renderResolvers(resolvers) {
  const cases = resolvers.map((resolver) => {
    const sourcePattern =
      resolver.sourceRef === undefined
        ? '_'
        : `.object _ ${leanString(resolver.sourceRef)}`;
    return (
      `| ${leanString(resolver.parentType)}, ${leanString(resolver.fieldName)}, ` +
      `${sourcePattern} => some (${renderResolverValue(resolver.returns)})`
    );
  });

  return [
    '{ resolve := fun parentType fieldName _arguments source =>',
    '    match parentType, fieldName, source with',
    ...cases.map((matchCase) => `    ${matchCase}`),
    '    | _, _, _ => some .null',
    '  resolve_argumentsEquivalent := by',
    '    intros',
    '    rfl',
    '}',
  ].join('\n');
}

function renderInputValue(value) {
  switch (value.kind) {
    case 'null':
      return '.null';
    case 'int':
      return `.int ${value.value}`;
    case 'float':
      return `.float ${leanString(value.value)}`;
    case 'string':
      return `.string ${leanString(value.value)}`;
    case 'boolean':
      return `.boolean ${value.value ? 'true' : 'false'}`;
    case 'enum':
      return `.enum ${leanString(value.value)}`;
    case 'variable':
      return `.variable ${leanString(value.name)}`;
    case 'list':
      return `.list (${leanList(value.values ?? [], renderInputValue, 2)})`;
    case 'object':
      return `.object (${leanList(
        value.fields ?? [],
        (field) => `(${leanString(field.name)}, ${renderInputValue(field.value)})`,
        2,
      )})`;
    default:
      throw new Error(`unsupported input value kind: ${value.kind}`);
  }
}

function renderConstInputValue(value) {
  switch (value.kind) {
    case 'null':
      return 'GraphQL.ConstInputValue.null';
    case 'int':
      return `GraphQL.ConstInputValue.int ${value.value}`;
    case 'float':
      return `GraphQL.ConstInputValue.float ${leanString(value.value)}`;
    case 'string':
      return `GraphQL.ConstInputValue.string ${leanString(value.value)}`;
    case 'boolean':
      return `GraphQL.ConstInputValue.boolean ${value.value ? 'true' : 'false'}`;
    case 'enum':
      return `GraphQL.ConstInputValue.enum ${leanString(value.value)}`;
    case 'list':
      return `GraphQL.ConstInputValue.list (${leanList(
        value.values ?? [],
        renderConstInputValue,
        2,
      )})`;
    case 'object':
      return `GraphQL.ConstInputValue.object (${leanList(
        value.fields ?? [],
        (field) => `(${leanString(field.name)}, ${renderConstInputValue(field.value)})`,
        2,
      )})`;
    default:
      throw new Error(`unsupported const input value kind: ${value.kind}`);
  }
}

function renderResolverValue(value) {
  switch (value.kind) {
    case 'null':
      return '.null';
    case 'scalar':
      return `.scalar ${leanString(String(value.value))}`;
    case 'object':
      return `.object ${leanString(value.typeName)} ${leanString(value.ref)}`;
    case 'list':
      return `.list (${leanList(value.values ?? [], renderResolverValue, 2)})`;
    case 'error':
      return failValue('resolver errors are represented by omitting a resolver value');
    default:
      throw new Error(`unsupported resolver value kind: ${value.kind}`);
  }
}

function renderResponseValue(value) {
  switch (value.kind) {
    case 'null':
      return '.null';
    case 'scalar':
      return `.scalar ${leanString(String(value.value))}`;
    case 'object':
      return `.object (${leanList(
        value.fields ?? [],
        (field) => `(${leanString(field.name)}, ${renderResponseValue(field.value)})`,
        2,
      )})`;
    case 'list':
      return `.list (${leanList(value.values ?? [], renderResponseValue, 2)})`;
    default:
      throw new Error(`unsupported response value kind: ${value.kind}`);
  }
}

function failValue(message) {
  throw new Error(message);
}

function renderNameList(names) {
  return leanList(names, leanString, 2);
}

function renderOptionString(value) {
  return value === undefined || value === null ? 'none' : `(some ${leanString(value)})`;
}

function leanList(items, renderItem, indentLevel) {
  if (items.length === 0) {
    return '[]';
  }
  const rendered = items.map(renderItem);
  const separator = `\n${' '.repeat(indentLevel)}, `;
  return `[ ${rendered[0]}${rendered.slice(1).map((item) => separator + item).join('')} ]`;
}

function leanString(value) {
  return JSON.stringify(String(value));
}

function indent(text, spaces) {
  const prefix = ' '.repeat(spaces);
  return text
    .split('\n')
    .map((line) => (line.length === 0 ? line : prefix + line))
    .join('\n');
}

function assertIdentifier(value, label) {
  if (!/^[A-Za-z][A-Za-z0-9_]*$/.test(value)) {
    throw new Error(`${label} is not a Lean-safe identifier: ${value}`);
  }
}
